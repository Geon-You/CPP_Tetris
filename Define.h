// 2차원 좌표를 가지는 구조체
typedef struct tPoint
{
    int x;
    int y;
    tPoint() {}
    tPoint(int x, int y)
        : x(x), y(y)
    {
    }
}Point;

typedef struct tInfo
{
    const char* name;
    Point pos;
    tInfo() {}
    tInfo(const char* name, int x, int y)
    {
        // 매개변수로 받은 문자열과 x, y 좌표로 Info 구조체 필드를 초기화
        this->name = name;
        pos = Point(x, y);
    }
}Info;

